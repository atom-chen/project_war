local MASK = "*"	-- 屏蔽掩码
----------------------------------------------------------------------
-- 功  能：屏蔽字符串
-- 参  数：str - 需要屏蔽的字符串
-- 返回值：第1个返回值 - 屏蔽后的字符串;第二个返回值 - 字符串是否被屏蔽标识
function KeywordShield(str)
	local dest = stringfilter_shield(str, MASK)
	return dest, dest ~= str
end
